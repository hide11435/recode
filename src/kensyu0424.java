public class kensyu0424 {
     public static void main(String[] args) {
         int a = 0; //aに0が入っている
         if (a == 1)  { //もしaが1であれば、A
             System.out.print("A");
         } else {  //そうでなければ、、B
             System.out.print("B");
         }
         System.out.print("C"); //　条件以外の場合は、、C

     } //elseが処理されたら、次のやつも処理可
}
